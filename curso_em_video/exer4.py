a = input('Digite algo: ')#a é um objeto todo abjeto tem atributos e metodos 
print('O tipo primitivo desse valor é {}'.format(type(a)))
print('Só tem espaço ? {}'.format(a.isspace()))
print('´E um numero  ? {} '.format(a.isnumeric()))
print('É alfabetico ? {}'.format(a.isalpha()))
print('É alfanumerico ? {}'.format(a.isalnum()))
print('Está em letra Maiuscula ? {}'.format(a.isupper()))
print('Esta em letra minuscula ? {}'.format(a.islower()))
print('Éstá capitalizado ? {}'.format(a.istitle()))
